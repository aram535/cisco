  ID BIGINT NOT NULL PRIMARY KEY,
  AUTHORIZATION_NUMBER VARCHAR(1000) DEFAULT NULL,
  VERSION INT DEFAULT NULL,
  DISTRIBUTOR_INFO VARCHAR(1000) DEFAULT NULL,
  START_DATE DATE DEFAULT NULL,
  END_DATE DATE DEFAULT NULL,
  DISTI_DISCOUNT DOUBLE DEFAULT NULL,
  RESELLER_NAME VARCHAR(1000) DEFAULT NULL,
  RESELLER_COUNTRY VARCHAR(1000) DEFAULT NULL,
  RESELLER_ACCT INT NOT NULL,
  END_USER_NAME VARCHAR(1000) DEFAULT NULL,
  END_USER_COUNTRY VARCHAR(1000) DEFAULT NULL,
  QUANTITY INT NOT NULL,
  CISCO_SKU VARCHAR(1000) DEFAULT NULL,
  DISTI_SKU VARCHAR(1000) DEFAULT NULL,
  LIST_PRICE VARCHAR(1000) DEFAULT NULL,
  CLAIM_UNIT VARCHAR(1000) DEFAULT NULL,
  EXT_CREDIT_AMT VARCHAR(1000) DEFAULT NULL,
  FAST_TRACK_PIE VARCHAR(1000) DEFAULT NULL,
  IP_NGN_PARTNER_PRICING_EM VARCHAR(1000) DEFAULT NULL,
  MDM_FULFILLMENT VARCHAR(1000) DEFAULT NULL
);